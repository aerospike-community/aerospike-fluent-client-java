package com.aerospike.query;

import com.aerospike.RecordResult;
import com.aerospike.client.Key;
import com.aerospike.client.Record;

public class SingleItemRecordStream implements RecordStreamImpl {
    private final RecordResult record;
    private boolean read = false;
    private boolean isFirstPage = true;
    
    public SingleItemRecordStream(RecordResult rec) {
        this.record = rec;
    }

    @Override
    public boolean hasMoreChunks() {
        if (isFirstPage) {
            isFirstPage = false;
            return true;
        }
        return false;
    }

    @Override
    public boolean hasNext() {
        if (!read) {
            read = true;
            return true;
        }
        return false;
    }

    @Override
    public RecordResult next() {
        return record;
    }

    @Override
    public void close() {
    }
}
